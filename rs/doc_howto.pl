<h3><a $atr_id="ending">勝敗の決定</a></h3>
<p class="paragraph">
村人側が人狼を全滅させるか、人間の人数が人狼の人数と同じまたはそれ以下にまで減るか、そのどちらかの条件を満たすと勝敗が決定し勝負が終わります。<br$net>
※Ｃ国狂人、ハムスター人間、コウモリ人間、ピクシーは人間にも人狼にも数えません。<br$net>
※Ｃ国狂人とハムスター人間陣営が両方生存している場合は、Ｃ国狂人を人間として数えます。
</p>

<p class="paragraph">
勝負が終わると、どの陣営が勝ったのかが表\示され、全員のIDと割り当てられた能\力が公開されます。また、独り言や囁きなど、勝負の最中には他人に見えないようになっていた発言も公開されます。
</p>

<p class="paragraph">
ここからはエピローグの時間です。明かされた全ての発言などを話の種にして、みんなで色々笑ったり嘆いたりしましょう。お疲れ様でした。
</p>
